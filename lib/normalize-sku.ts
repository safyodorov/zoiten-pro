// lib/normalize-sku.ts
// Трансформирует различные форматы УКТ в канонический вид "УКТ-000001".
// Pure функция без внешних зависимостей.
// Source: REQUIREMENTS.md STOCK-03; Phase 14 Research Pattern 4.

// U+2014 — em-dash, встречается в Excel при автозамене дефиса
const EM_DASH = "\u2014"

// Матчит: "УКТ-000001", "УКТ000001", "000001", "1"
// Опциональный префикс УКТ (с дефисом или без), затем одна и более цифр
const SKU_REGEX = /^(?:УКТ-?)?(\d+)$/

/**
 * Нормализует УКТ-код товара к каноническому виду "УКТ-000001".
 *
 * Поддерживаемые форматы входа:
 *   "УКТ-000001"      → "УКТ-000001" (уже канонический)
 *   "УКТ-1"           → "УКТ-000001" (padStart до 6 цифр)
 *   "1"               → "УКТ-000001" (только цифры, без префикса)
 *   " укт-000001 "    → "УКТ-000001" (trim + toUpperCase)
 *   "УКТ\u2014000001" → "УКТ-000001" (em-dash → дефис)
 *
 * @throws {Error} если строка не является валидным УКТ-кодом
 */
export function normalizeSku(raw: string): string {
  const s = raw.trim().toUpperCase().replace(EM_DASH, "-")
  const match = SKU_REGEX.exec(s)
  if (!match || !match[1]) {
    throw new Error(`Невалидный УКТ: "${raw}"`)
  }
  return `УКТ-${match[1].padStart(6, "0")}`
}
